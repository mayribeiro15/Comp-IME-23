    private void jButtonsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsalvarActionPerformed
        OM om = new OM();
        ControllerOM controle = new ControllerOM();

        om.setOm(jTextOm.getText());
        om.setId(Integer.parseInt(jLIdOm.getText()));

        try {
            if (validarCampos() == false) {
                JOptionPane.showMessageDialog(null, "CAMPO OBRIGATÓRIO");
                jLIdOm.setForeground(Color.red);
            } else {
                controle.altetar(om);
                JOptionPane.showMessageDialog(null, "OM ALTERADA");
                dispose();
            }
        } catch (Exception ex) {
            Logger.getLogger(TlAltOM.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonsalvarActionPerformed

