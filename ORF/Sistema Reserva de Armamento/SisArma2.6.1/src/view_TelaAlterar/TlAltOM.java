    private void jTextBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextBuscaKeyReleased
        if (jTextBusca.getText().length() == 0) {
            DefaultTableModel tabela = (DefaultTableModel) jTableOM.getModel();
            tabela.setNumRows(0);
        } else {
            listaBairro();
        }
    }//GEN-LAST:event_jTextBuscaKeyReleased

    private void jTableOMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableOMMouseClicked
        if(evt.getClickCount() == 2){
        int linha = jTableOM.getSelectedRow();
        jLIdOm.setText(String.valueOf(jTableOM.getValueAt(linha, 0)));
        jTextOm.setText(String.valueOf(jTableOM.getValueAt(linha, 1)));
        jButtonEditar.setEnabled(true);
        }
    }//GEN-LAST:event_jTableOMMouseClicked

    private void limparCampos() {
        jTextBusca.setText("");
    }

    private boolean validarCampos() {
        boolean valida = true;
        if (jTextOm.getText().equals("")) {
            valida = false;
        }
        return valida;
    }

    private void listaBairro() {
        DefaultTableModel tabela = (DefaultTableModel) jTableOM.getModel();
        tabela.setNumRows(0);
        String nome = jTextBusca.getText();
        OMDao omDao = new OMDao();
        List<OM> lista;

        try {
            lista = omDao.listaOmPorNome(nome);
            for (OM om : lista) {
                tabela.addRow(new Object[]{om.getId(), om.getOm()});
            }
        } catch (Exception ex) {
            System.out.println("Erro ao listar OM" + ex.getMessage());
        }
    }


