    private void verificaLista() {
        String Armeiro = "ARMEIRO";
        String Inspecionador = "INSPECIONADOR";
        String OficialDia = "OFICIAL DIA";
        String Ativo = "ATIVO";
        String Inativo = "INATIVO";
        try {
            List<FuncaoMilitar> lista = new FuncaoMilitarDao().buscaFuncoes(jTidentidade.getText());
            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Militar sem função");
                jTidentidade.setText("");
            } else {
                jTidentidade.setEditable(false);
                for (FuncaoMilitar f : lista) {

                    if (f.getFuncao().getFuncao().equals(Armeiro) && f.getStatus().equals(Ativo)) {
                        JOptionPane.showMessageDialog(null, "FUNÇÃO ARMEIRO - ATIVO");
                        idArmeiro = f.getId();
                        jRadioBativoarmeiro.setSelected(true);
                        jRadioArmeiro.setEnabled(true);
                    }
                    if (f.getFuncao().getFuncao().equals(Armeiro) && f.getStatus().equals(Inativo)) {
                        JOptionPane.showMessageDialog(null, "FUNÇÃO ARMEIRO - INATIVO");
                        idArmeiro = f.getId();
                        jRadioBinativoarmeiro.setSelected(true);
                        jRadioArmeiro.setEnabled(true);
                    }
                    if (f.getFuncao().getFuncao().equals(Inspecionador) && f.getStatus().equals(Ativo)) {
                        JOptionPane.showMessageDialog(null, "FUNÇÃO INSPECIONADOR - ATIVO");
                        idInspecionador = f.getId();
                        jRadioBativoinspecionador.setSelected(true);
                        JRadioInspecionador.setEnabled(true);

                    }
                    if (f.getFuncao().getFuncao().equals(Inspecionador) && f.getStatus().equals(Inativo)) {
                        JOptionPane.showMessageDialog(null, "FUNÇÃO INSPECIONADOR - INATIVO");
                        idInspecionador = f.getId();
                        jRadioBinativoinspecionador.setSelected(true);
                        JRadioInspecionador.setEnabled(true);

                    }
                    if (f.getFuncao().getFuncao().equals(OficialDia) && f.getStatus().equals(Ativo)) {
                        JOptionPane.showMessageDialog(null, "FUNÇÃO OFICIAL DIA - ATIVO");
                        idOficialDia = f.getId();
                        jRadioBativooficialdia.setSelected(true);
                        JRadioOficialDia.setEnabled(true);
                    }
                    if (f.getFuncao().getFuncao().equals(OficialDia) && f.getStatus().equals(Inativo)) {
                        JOptionPane.showMessageDialog(null, "FUNÇÃO OFICIAL DIA - INATIVO");
                        idOficialDia = f.getId();
                        jRadioBinativooficialdia.setSelected(true);
                        JRadioOficialDia.setEnabled(true);
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Armaria.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

