    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        FuncaoMilitar funcao = new FuncaoMilitar();
        FuncaoMilitarDao fdao = new FuncaoMilitarDao();
        if (jRadioArmeiro.isSelected()) {
            funcao.setId(idArmeiro);
            funcao.setStatus(statusArmeiro());
        }
        if (JRadioInspecionador.isSelected()) {
            funcao.setId(idInspecionador);
            funcao.setStatus(statusInspecionador());
        }
        if (JRadioOficialDia.isSelected()) {
            funcao.setId(idOficialDia);
            funcao.setStatus(statusOficialDia());
        }
        try {
            fdao.alterar(funcao);
            JOptionPane.showMessageDialog(null, "ALTERADO");
            dispose();
        } catch (Exception ex) {
            Logger.getLogger(TlAltFuncaoMilitar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBsalvarActionPerformed

